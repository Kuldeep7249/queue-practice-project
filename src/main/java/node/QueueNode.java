package node;

public class DoublyLinkedListNode {
    public int data;
    public DoublyLinkedListNode next;

    public DoublyLinkedListNode(int data) {
        this.data = data;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "NodeQueue{" +
                "data=" + data +
                ", next=" + next +
                '}';
    }
}
